    @Override
    public void update(java.util.Observable updatedModel,Object parametros) {
        if (parametros != UserModel.USUARIO_CURRENT) return;
       
       Usuario clienteCurrent = model.getUsuarioCurrent();
        
       this.nombreTex.setEnabled(model.getModo()==Application.MODO_AGREGAR);
       
       nombreTex.setText(clienteCurrent.getNombre());
       if (model.getErrores().get("nombre")!=null){
            nombrelbl.setBorder(Application.BORDER_ERROR);
            nombrelbl.setToolTipText(model.getErrores().get("nombre"));
        }
        else{
            nombrelbl.setBorder(null);
            nombrelbl.setToolTipText("");
        }

        this.contaseTex.setText(clienteCurrent.getContrase単a());
        if (model.getErrores().get("contrase単a")!=null){
            this.contraselbl.setBorder(Application.BORDER_ERROR);
            contraselbl.setToolTipText(model.getErrores().get("contrase単a"));
        }
        else{
           contraselbl.setBorder(null);
           contraselbl.setToolTipText("");
        }
        if(clienteCurrent.getTipo()=="super"){
            this.administradorRB.setSelected(true);
        }
        else
            this.dependienteRB.setSelected(true);
        
        this.validate();
        if (!model.getMensaje().equals("")){
            JOptionPane.showMessageDialog(this, model.getMensaje(), "",JOptionPane.INFORMATION_MESSAGE);
        }
    }
