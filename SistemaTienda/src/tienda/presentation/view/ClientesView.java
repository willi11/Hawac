    private void clientesFldMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clientesFldMouseClicked
         if (evt.getClickCount() == 2) {
         int row = this.clientesFld.getSelectedRow();
         Application.CLIENTE_VIEW.setLocation(evt.getLocationOnScreen());
         controller.editar(row);
      }        // TODO add your handling code here:
    }//GEN-LAST:event_clientesFldMouseClicked

    private void apartadosBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apartadosBtnActionPerformed
       
        int row = this.clientesFld.getSelectedRow();
        if (!this.clientesFld.isRowSelected(row)) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "ERROR AL MOSTRAR, DEBE HABER SELECCIONADO UN CLIENTE", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else{
            controller.listarApartados(row);
        }
        
    }//GEN-LAST:event_apartadosBtnActionPerformed

